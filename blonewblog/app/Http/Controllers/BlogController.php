     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $blog= new Blog;
        $blog->name=request('name');
        $blog->age=request('age');
        $blog->class=request('class');
        $blog->save();
        return  redirect('/blogs');
    }

